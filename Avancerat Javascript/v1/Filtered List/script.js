function createListItem(str) {
    return React.createElement('li', {
        key: str
    }, str);
}
class FilterList extends React.Component {
    constructor(props) {
        super(props);

        this.state = {
            value: "",
            reversed: false // Ikke reverse= standard
        };

        this.onChange = this.onChange.bind(this);
        this.onClick = this.onClick.bind(this); 
    }

    onChange(e) {
        this.setState({
            value: e.target.value.toLowerCase()
        })
    }

    onClick(){
        COUNTRY.reverse();
        this.setState({reversed: !this.state.reversed}); //om den är false så blir den true.  vice versa
    }

    render() {
        const input = React.createElement("input", {
            placeholder: "Filter Country",
            type: "text",
            onChange: this.onChange
        });
        const btn = React.createElement("button", {onClick: this.onClick}, "Click");
        const list = React.createElement(
            'ul', {},
            COUNTRY.filter(country => country.toLowerCase().includes(this.state.value)).map(createListItem),
        );


        return React.createElement("div", {}, input, btn, list);
    }

}
ReactDOM.render(
    React.createElement(FilterList),
    document.getElementById('target')
);